public class Company
{
    private int id;
    private string name;
    private int employees;


    public int Id
    {
        get { return id; }
        set { id = value; }
    }

    public string Name
    {
        get { return name; }
        set { name = value; }
    }

    public int Employees
    {
        get { return employees; }
        set { employees = value; }
       
    }
}